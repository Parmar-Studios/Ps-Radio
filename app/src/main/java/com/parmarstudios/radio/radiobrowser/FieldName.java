package com.parmarstudios.radio.radiobrowser;

/**
 * The field name for ordering.
 *
 * @author Manish Parmar
 */

public enum FieldName {

    /**
     * The station name.
     */
    NAME,

    /**
     * The station url.
     */
    URL,

    /**
     * The station homepage.
     */
    HOMEPAGE,

    /**
     * The station favicon.
     */
    FAVICON,

    /**
     * The station tags.
     */
    TAGS,

    /**
     * The station country.
     */
    COUNTRY,

    /**
     * The station state.
     */
    STATES,

    /**
     * The station language.
     */
    LANGUAGE,

    /**
     * The station votes.
     */
    VOTES,

    /**
     * The station codecs.
     */
    CODEC,

    /**
     * The station bitrate.
     */
    BITRATE,

    /**
     * Weather the last check was ok.
     */
    LASTCHECKOK,

    /**
     * Last check time.
     */
    LASTCHECKTIME,

    /**
     * The click time stamp.
     */
    CLICKTIMESTAMP,

    /**
     * The station click count.
     */
    CICKCOUNT,

    /**
     * The station click trend.
     */
    CLICKTREND

}
